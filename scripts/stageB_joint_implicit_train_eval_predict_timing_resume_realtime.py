def default_device() -> torch.device:
    return torch.device("cuda" if torch.cuda.is_available() else "cpu")


def synchronize_device(device: torch.device) -> None:
    """Synchronize CUDA before/after timing to avoid asynchronous underestimation."""
    if device.type == "cuda" and torch.cuda.is_available():
        torch.cuda.synchronize(device)


def start_device_timer(device: torch.device) -> float:
    synchronize_device(device)
    return time.perf_counter()


def stop_device_timer(start_time: float, device: torch.device) -> float:
    synchronize_device(device)
    return float(time.perf_counter() - start_time)


def move_optimizer_state_to_device(optimizer: torch.optim.Optimizer, device: torch.device) -> None:
    """Move restored optimizer tensors to the active device after checkpoint loading."""
    for state in optimizer.state.values():
        for key, value in list(state.items()):
            if torch.is_tensor(value):
                state[key] = value.to(device)


def current_learning_rates(optimizer: torch.optim.Optimizer) -> List[float]:
    return [float(group.get("lr", float("nan"))) for group in optimizer.param_groups]



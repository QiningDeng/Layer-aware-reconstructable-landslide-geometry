def rasterize_polygons(polygons: Sequence[np.ndarray], width: int, height: int) -> np.ndarray:
    mask = np.zeros((height, width), dtype=np.uint8)
    valid = []
    for poly in polygons:
        arr = np.asarray(poly, dtype=np.float32).reshape(-1, 2)
        if arr.shape[0] >= 3:
            valid.append(np.round(arr).astype(np.int32).reshape(-1, 1, 2))
    if valid:
        cv2.fillPoly(mask, valid, 255)
    return mask



def mask_to_polygons(mask: np.ndarray, epsilon_ratio: float = 0.002, min_area: float = 4.0) -> List[np.ndarray]:
    mask_u8 = (mask > 0).astype(np.uint8) * 255
    contours, _ = cv2.findContours(mask_u8, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    polygons: List[np.ndarray] = []
    for cnt in contours:
        area = float(cv2.contourArea(cnt))
        if area < min_area:
            continue
        epsilon = epsilon_ratio * cv2.arcLength(cnt, True)
        approx = cv2.approxPolyDP(cnt, epsilon, True)
        pts = approx.reshape(-1, 2).astype(np.float32)
        if pts.shape[0] >= 3:
            polygons.append(pts)
    return polygons



def polygons_to_json_layers(sample_name: str, source_image: str, layer_names: Sequence[str], vertical_ranks: Sequence[str], polygons_by_layer: Sequence[List[np.ndarray]], grid_size: Tuple[int, int]) -> Dict[str, Any]:
    layers = []
    for layer_name, rank, polys in zip(layer_names, vertical_ranks, polygons_by_layer):
        layers.append(
            {
                "name": layer_name,
                "vertical_rank": rank,
                "polygon_count": len(polys),
                "polygons": [
                    {"points": [[float(x), float(y)] for x, y in poly.tolist()]} for poly in polys
                ],
            }
        )
    return {
        "sample_name": sample_name,
        "source_image": source_image,
        "canvas_size": {"width": int(grid_size[0]), "height": int(grid_size[1])},
        "layers": layers,
    }



def extract_boundary_points(mask: np.ndarray) -> np.ndarray:
    mask_u8 = (mask > 0).astype(np.uint8) * 255
    contours, _ = cv2.findContours(mask_u8, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_NONE)
    pts = []
    for cnt in contours:
        if cnt.shape[0] >= 2:
            pts.append(cnt.reshape(-1, 2).astype(np.float32))
    if not pts:
        return np.zeros((0, 2), dtype=np.float32)
    return np.concatenate(pts, axis=0)



def chamfer_distance(a: np.ndarray, b: np.ndarray) -> float:
    if len(a) == 0 and len(b) == 0:
        return 0.0
    if len(a) == 0 or len(b) == 0:
        return float("inf")
    diff = a[:, None, :] - b[None, :, :]
    dist = np.sqrt(np.sum(diff ** 2, axis=2))
    return float(dist.min(axis=1).mean() + dist.min(axis=0).mean()) / 2.0



def hausdorff_distance(a: np.ndarray, b: np.ndarray) -> float:
    if len(a) == 0 and len(b) == 0:
        return 0.0
    if len(a) == 0 or len(b) == 0:
        return float("inf")
    diff = a[:, None, :] - b[None, :, :]
    dist = np.sqrt(np.sum(diff ** 2, axis=2))
    return float(max(dist.min(axis=1).max(), dist.min(axis=0).max()))



def centroid_from_mask(mask: np.ndarray) -> Tuple[float, float]:
    ys, xs = np.where(mask > 0)
    if len(xs) == 0:
        return float("nan"), float("nan")
    return float(xs.mean()), float(ys.mean())



def compute_layer_metrics(pred_mask: np.ndarray, gt_mask: np.ndarray, pred_polygons: List[np.ndarray], gt_polygons: List[np.ndarray], diagonal: float) -> Dict[str, float]:
    pred_bin = (pred_mask > 0).astype(np.uint8)
    gt_bin = (gt_mask > 0).astype(np.uint8)

    inter = float(np.logical_and(pred_bin > 0, gt_bin > 0).sum())
    union = float(np.logical_or(pred_bin > 0, gt_bin > 0).sum())
    pred_area = float((pred_bin > 0).sum())
    gt_area = float((gt_bin > 0).sum())

    iou = inter / union if union > 0 else 1.0
    dice = (2.0 * inter) / (pred_area + gt_area) if (pred_area + gt_area) > 0 else 1.0
    area_rel_err = abs(pred_area - gt_area) / gt_area if gt_area > 0 else 0.0

    pred_centroid = centroid_from_mask(pred_bin)
    gt_centroid = centroid_from_mask(gt_bin)
    if any(math.isnan(v) for v in pred_centroid + gt_centroid):
        centroid_dist = 1.0 if (pred_area > 0 or gt_area > 0) else 0.0
    else:
        centroid_dist = min(1.0, math.dist(pred_centroid, gt_centroid) / max(diagonal, 1e-6))

    pred_bd = extract_boundary_points(pred_bin)
    gt_bd = extract_boundary_points(gt_bin)
    chamfer = chamfer_distance(pred_bd, gt_bd)
    hausdorff = hausdorff_distance(pred_bd, gt_bd)
    if math.isinf(chamfer) or math.isnan(chamfer):
        chamfer_norm = 1.0 if (pred_area > 0 or gt_area > 0) else 0.0
    else:
        chamfer_norm = min(1.0, chamfer / max(diagonal, 1e-6))
    if math.isinf(hausdorff) or math.isnan(hausdorff):
        hausdorff_norm = 1.0 if (pred_area > 0 or gt_area > 0) else 0.0
    else:
        hausdorff_norm = min(1.0, hausdorff / max(diagonal, 1e-6))

    return {
        "mask_iou": float(iou),
        "mask_dice": float(dice),
        "area_relative_error": float(area_rel_err),
        "centroid_distance_normalized": float(centroid_dist),
        "boundary_chamfer_normalized": float(chamfer_norm),
        "boundary_hausdorff_normalized": float(hausdorff_norm),
        "polygon_count_abs_diff": float(abs(len(pred_polygons) - len(gt_polygons))),
    }



def build_overlay_image(gt_masks: np.ndarray, pred_masks: np.ndarray) -> np.ndarray:
    # gt_masks and pred_masks: [3, H, W]
    h, w = gt_masks.shape[1:]
    canvas = np.zeros((h, w, 3), dtype=np.uint8)
    gt_colors = [(255, 0, 0), (0, 255, 0), (0, 255, 255)]
    pred_colors = [(128, 0, 0), (0, 128, 0), (0, 128, 255)]

    for i in range(3):
        gt_contours, _ = cv2.findContours((gt_masks[i] > 0).astype(np.uint8) * 255, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        pred_contours, _ = cv2.findContours((pred_masks[i] > 0).astype(np.uint8) * 255, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        cv2.drawContours(canvas, gt_contours, -1, gt_colors[i], 1)
        cv2.drawContours(canvas, pred_contours, -1, pred_colors[i], 1)
    return canvas



@torch.no_grad()
def predict_dense_fields(
    decoder: JointImplicitDecoder,
    latent: torch.Tensor,
    grid_size: int,
    device: torch.device,
    chunk_size: int,
) -> Dict[str, np.ndarray]:
    decoder.eval()
    coords = build_coordinate_grid(grid_size, grid_size, device=device).view(-1, 2)
    outputs_sdf: List[torch.Tensor] = []
    outputs_corner: List[torch.Tensor] = []
    outputs_mask: List[torch.Tensor] = []

    for start in range(0, coords.shape[0], chunk_size):
        end = min(coords.shape[0], start + chunk_size)
        coord_chunk = coords[start:end][None, ...]
        pred = decoder(latent, coord_chunk)
        outputs_sdf.append(pred["sdf"].squeeze(0).cpu())
        outputs_corner.append(torch.sigmoid(pred["corner_logits"]).squeeze(0).cpu())
        outputs_mask.append(torch.sigmoid(pred["mask_logits"]).squeeze(0).cpu())

    sdf = torch.cat(outputs_sdf, dim=0).view(grid_size, grid_size, 3).permute(2, 0, 1).numpy().astype(np.float32)
    corner = torch.cat(outputs_corner, dim=0).view(grid_size, grid_size, 3).permute(2, 0, 1).numpy().astype(np.float32)
    mask_prob = torch.cat(outputs_mask, dim=0).view(grid_size, grid_size, 3).permute(2, 0, 1).numpy().astype(np.float32)
    return {"sdf": sdf, "corner": corner, "mask_prob": mask_prob}



def reconstruct_polygons_from_sdf_fields(sdf: np.ndarray, epsilon_ratio: float = 0.002, min_area: float = 4.0) -> Tuple[np.ndarray, List[List[np.ndarray]]]:
    # sdf: [3, H, W]; inside <= 0
    pred_masks = (sdf <= 0.0).astype(np.uint8)
    polygons_by_layer: List[List[np.ndarray]] = []
    for i in range(pred_masks.shape[0]):
        polygons_by_layer.append(mask_to_polygons(pred_masks[i], epsilon_ratio=epsilon_ratio, min_area=min_area))
    return pred_masks, polygons_by_layer



def evaluate_single_prediction(
    pred_sdf: np.ndarray,
    gt_masks: np.ndarray,
    gt_polygons_by_layer: Sequence[List[np.ndarray]],
    layer_names: Sequence[str],
    vertical_ranks: Sequence[str],
) -> Tuple[List[Dict[str, Any]], Dict[str, float], np.ndarray, List[List[np.ndarray]]]:
    pred_masks, pred_polygons_by_layer = reconstruct_polygons_from_sdf_fields(pred_sdf)
    h, w = gt_masks.shape[1:]
    diagonal = float(math.hypot(w, h))
    rows: List[Dict[str, Any]] = []

    for i in range(3):
        metrics = compute_layer_metrics(pred_masks[i], gt_masks[i], pred_polygons_by_layer[i], gt_polygons_by_layer[i], diagonal)
        rows.append(
            {
                "layer_name": layer_names[i],
                "vertical_rank": vertical_ranks[i],
                **metrics,
            }
        )

    metric_names = [
        "mask_iou",
        "mask_dice",
        "area_relative_error",
        "centroid_distance_normalized",
        "boundary_chamfer_normalized",
        "boundary_hausdorff_normalized",
        "polygon_count_abs_diff",
    ]
    summary = {k: float(np.mean([row[k] for row in rows])) for k in metric_names}
    return rows, summary, pred_masks, pred_polygons_by_layer



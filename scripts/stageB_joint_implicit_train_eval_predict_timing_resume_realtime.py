def train_one_epoch_vae(
    encoder: ConvEncoder,
    decoder: JointImplicitDecoder,
    loader: DataLoader,
    optimizer: torch.optim.Optimizer,
    cfg: TrainConfig,
    device: torch.device,
) -> Dict[str, float]:
    encoder.train()
    decoder.train()
    totals: Dict[str, List[float]] = {k: [] for k in ["total", "sdf", "corner", "mask", "sep", "consistency", "kl"]}

    h = w = int(next(iter(loader))["sdf"].shape[-1])
    coords_flat = build_coordinate_grid(h, w, device=device).view(-1, 2)

    for batch in loader:
        feature_small = batch["feature_small"].to(device)
        sdf = batch["sdf"].to(device)
        corner = batch["corner"].to(device)
        mask = batch["mask"].to(device)

        sampled = sample_random_points(sdf, corner, mask, coords_flat, cfg.points_per_sample)
        optimizer.zero_grad(set_to_none=True)
        mu, logvar = encoder(feature_small)
        z = reparameterize(mu, logvar)
        outputs = decoder(z, sampled["coords"])
        loss_dict = total_reconstruction_loss(outputs, sampled["sdf"], sampled["corner"], sampled["mask"], cfg, mu=mu, logvar=logvar)
        loss_dict["total"].backward()
        optimizer.step()

        for k in totals:
            totals[k].append(float(loss_dict[k].detach().cpu()))

    return {f"train_{k}_loss": float(np.mean(v)) for k, v in totals.items()}




def summarize_timing_rows(rows: Sequence[Dict[str, Any]], keys: Sequence[str]) -> Dict[str, Dict[str, float]]:
    summary: Dict[str, Dict[str, float]] = {}
    for key in keys:
        vals: List[float] = []
        for row in rows:
            value = row.get(key)
            if value is None:
                continue
            try:
                value_f = float(value)
            except (TypeError, ValueError):
                continue
            if np.isfinite(value_f):
                vals.append(value_f)
        if not vals:
            summary[key] = {
                "count": 0, "mean": float("nan"), "std": float("nan"),
                "median": float("nan"), "p95": float("nan"),
                "min": float("nan"), "max": float("nan"),
            }
            continue
        arr = np.asarray(vals, dtype=np.float64)
        summary[key] = {
            "count": int(arr.size),
            "mean": float(arr.mean()),
            "std": float(arr.std(ddof=0)),
            "median": float(np.median(arr)),
            "p95": float(np.percentile(arr, 95)),
            "min": float(arr.min()),
            "max": float(arr.max()),
        }
    return summary





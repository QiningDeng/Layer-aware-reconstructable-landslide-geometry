@torch.no_grad()
def validate_vae(
    encoder: ConvEncoder,
    decoder: JointImplicitDecoder,
    loader: DataLoader,
    cfg: TrainConfig,
    device: torch.device,
) -> Dict[str, float]:
    encoder.eval()
    decoder.eval()

    loss_totals: Dict[str, List[float]] = {k: [] for k in ["total", "sdf", "corner", "mask", "sep", "consistency", "kl"]}
    sample_rows: List[Dict[str, Any]] = []

    for batch in loader:
        feature_small = batch["feature_small"].to(device)
        sdf = batch["sdf"].to(device)
        corner = batch["corner"].to(device)
        mask = batch["mask"].to(device)
        mu, logvar = encoder(feature_small)
        z = mu  # deterministic path for validation

        coords_flat = build_coordinate_grid(
            sdf.shape[-2], sdf.shape[-1], device=device
        ).view(-1, 2)
        sampled = sample_random_points(
            sdf, corner, mask, coords_flat, cfg.points_per_sample
        )
        outputs = decoder(z, sampled["coords"])
        loss_dict = total_reconstruction_loss(outputs, sampled["sdf"], sampled["corner"], sampled["mask"], cfg, mu=mu, logvar=logvar)
        for k in loss_totals:
            loss_totals[k].append(float(loss_dict[k].detach().cpu()))

        # Dense reconstruction per sample.
        for i in range(feature_small.shape[0]):
            latent = z[i:i + 1]
            dense = predict_dense_fields(decoder, latent, cfg.eval_grid_size, device, cfg.eval_chunk_size)
            gt_masks = F.interpolate(mask[i:i + 1], size=(cfg.eval_grid_size, cfg.eval_grid_size), mode="nearest").squeeze(0).cpu().numpy().astype(np.uint8)
            gt_polygons = [mask_to_polygons(gt_masks[c]) for c in range(3)]
            rows, summary, _, _ = evaluate_single_prediction(
                dense["sdf"],
                gt_masks,
                gt_polygons,
                batch["layer_names"][i],
                batch["vertical_ranks"][i],
            )
            sample_rows.append(summary)

    metric_summary = summarize_numeric_rows(sample_rows, [
        "mask_iou",
        "mask_dice",
        "area_relative_error",
        "centroid_distance_normalized",
        "boundary_chamfer_normalized",
        "boundary_hausdorff_normalized",
        "polygon_count_abs_diff",
    ])

    result = {f"val_{k}_loss": float(np.mean(v)) for k, v in loss_totals.items()}
    for k in metric_summary:
        mean_v = float(metric_summary[k]["mean"])
        if math.isnan(mean_v) or math.isinf(mean_v):
            mean_v = 1.0 if ("iou" not in k and "dice" not in k) else 0.0
        result[f"val_mean_{k}"] = mean_v
    return result




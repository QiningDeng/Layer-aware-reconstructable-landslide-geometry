def summarize_numeric_rows(rows: Sequence[Dict[str, Any]], keys: Sequence[str]) -> Dict[str, Dict[str, float]]:
    summary: Dict[str, Dict[str, float]] = {}
    for key in keys:
        vals = []
        for row in rows:
            if key not in row or row[key] is None:
                continue
            v = float(row[key])
            if math.isnan(v) or math.isinf(v):
                continue
            vals.append(v)
        if not vals:
            summary[key] = {"count": 0, "mean": float("nan"), "std": float("nan"), "min": float("nan"), "max": float("nan")}
        else:
            arr = np.asarray(vals, dtype=np.float64)
            summary[key] = {
                "count": int(arr.size),
                "mean": float(arr.mean()),
                "std": float(arr.std(ddof=0)),
                "min": float(arr.min()),
                "max": float(arr.max()),
            }
    return summary



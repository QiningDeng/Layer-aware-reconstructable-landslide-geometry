def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Stage B training, continuation, evaluation, and prediction for "
            "layered implicit reconstruction."
        )
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    train_parser = subparsers.add_parser(
        "train", help="Train a new model or continue an existing run."
    )
    train_parser.add_argument("--feature_root", type=str, default="")
    train_parser.add_argument("--output_root", type=str, default="")
    train_parser.add_argument(
        "--model_type", type=str, default="vae", choices=["vae", "autodecoder"]
    )
    train_parser.add_argument("--latent_dim", type=int, default=64)
    train_parser.add_argument("--encoder_input_size", type=int, default=128)
    train_parser.add_argument("--eval_grid_size", type=int, default=256)
    train_parser.add_argument("--batch_size", type=int, default=4)
    train_parser.add_argument(
        "--epochs",
        type=int,
        default=160,
        help="Target total epoch. When resuming, this is not an additional epoch count.",
    )
    train_parser.add_argument("--learning_rate", type=float, default=5e-5)
    train_parser.add_argument("--latent_learning_rate", type=float, default=5e-3)
    train_parser.add_argument("--points_per_sample", type=int, default=4096)
    train_parser.add_argument("--seed", type=int, default=42)
    train_parser.add_argument("--num_workers", type=int, default=0)
    train_parser.add_argument("--cache_in_memory", action="store_true")
    train_parser.add_argument("--patience", type=int, default=25)
    train_parser.add_argument("--save_every", type=int, default=10)
    train_parser.add_argument("--plot_every", type=int, default=10)
    train_parser.add_argument(
        "--scheduler_type",
        choices=["none", "plateau"],
        default="none",
    )
    train_parser.add_argument("--scheduler_factor", type=float, default=0.5)
    train_parser.add_argument("--scheduler_patience", type=int, default=10)
    train_parser.add_argument("--min_learning_rate", type=float, default=1e-7)
    train_parser.add_argument(
        "--resume_checkpoint",
        type=str,
        default="",
        help="Prefer last_model.pt when extending a capped run.",
    )
    train_parser.add_argument(
        "--resume_reset_optimizer",
        action="store_true",
        help="Continue model weights but rebuild optimizer/scheduler states.",
    )
    train_parser.add_argument(
        "--no_final_evaluation",
        action="store_true",
        help="Skip automatic validation evaluation after training.",
    )
    train_parser.add_argument(
        "--evaluate_test_after_training",
        action="store_true",
        help=(
            "Also evaluate the test split after training. Leave disabled while "
            "selecting latent dimensions to avoid repeated test-set inspection."
        ),
    )

    eval_parser = subparsers.add_parser(
        "evaluate", help="Evaluate a saved checkpoint on a selected split."
    )
    eval_parser.add_argument("--checkpoint", type=str, default="")
    eval_parser.add_argument("--feature_root", type=str, default="")
    eval_parser.add_argument("--output_root", type=str, default="")
    eval_parser.add_argument(
        "--split_name", type=str, default="test", choices=["train", "val", "test"]
    )

    pred_parser = subparsers.add_parser(
        "predict", help="Run prediction export with a saved checkpoint."
    )
    pred_parser.add_argument("--checkpoint", type=str, default="")
    pred_parser.add_argument("--feature_root", type=str, default="")
    pred_parser.add_argument("--output_root", type=str, default="")
    pred_parser.add_argument(
        "--split_name", type=str, default="test", choices=["train", "val", "test"]
    )

    return parser


def args_to_config(args: argparse.Namespace) -> TrainConfig:
    return TrainConfig(
        model_type=args.model_type,
        feature_root=args.feature_root,
        output_root=args.output_root,
        resume_checkpoint=args.resume_checkpoint,
        resume_reset_optimizer=args.resume_reset_optimizer,
        latent_dim=args.latent_dim,
        encoder_input_size=args.encoder_input_size,
        eval_grid_size=args.eval_grid_size,
        batch_size=args.batch_size,
        epochs=args.epochs,
        learning_rate=args.learning_rate,
        latent_learning_rate=args.latent_learning_rate,
        points_per_sample=args.points_per_sample,
        seed=args.seed,
        num_workers=args.num_workers,
        cache_in_memory=args.cache_in_memory,
        patience=args.patience,
        save_every=args.save_every,
        plot_every=args.plot_every,
        scheduler_type=args.scheduler_type,
        scheduler_factor=args.scheduler_factor,
        scheduler_patience=args.scheduler_patience,
        min_learning_rate=args.min_learning_rate,
        run_final_evaluation=not args.no_final_evaluation,
        evaluate_test_after_training=args.evaluate_test_after_training,
    )


def main() -> None:
    parser = build_parser()
    args = parser.parse_args()

    if args.command == "train":
        if not args.resume_checkpoint:
            if not args.feature_root:
                print("Please select the Stage A output root folder.")
                args.feature_root = select_folder(
                    "Select the Stage A output root folder"
                )
            if not args.output_root:
                print("Please select the Stage B output root folder.")
                args.output_root = select_folder(
                    "Select the Stage B output root folder"
                )
        cfg = args_to_config(args)
        train_model(cfg)
    elif args.command in {"evaluate", "predict"}:
        if not args.checkpoint:
            print("Please select a saved checkpoint file.")
            args.checkpoint = select_file(
                "Select a saved checkpoint",
                [("PyTorch checkpoint", "*.pt"), ("All files", "*.*")],
            )
        if not args.feature_root:
            print("Please select the Stage A output root folder.")
            args.feature_root = select_folder(
                "Select the Stage A output root folder"
            )
        if not args.output_root:
            print("Please select the Stage B output root folder.")
            args.output_root = select_folder(
                "Select the Stage B output root folder"
            )
        evaluate_checkpoint_on_split(
            args.checkpoint,
            args.feature_root,
            args.output_root,
            split_name=args.split_name,
            predict_only=(args.command == "predict"),
        )
    else:
        raise ValueError(f"Unsupported command: {args.command}")


if __name__ == "__main__":
    main()

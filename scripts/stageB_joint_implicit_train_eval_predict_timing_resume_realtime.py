def reparameterize(mu: torch.Tensor, logvar: torch.Tensor) -> torch.Tensor:
    std = torch.exp(0.5 * logvar)
    eps = torch.randn_like(std)
    return mu + eps * std



def kl_divergence(mu: torch.Tensor, logvar: torch.Tensor) -> torch.Tensor:
    return 0.5 * torch.mean(torch.sum(torch.exp(logvar) + mu ** 2 - 1.0 - logvar, dim=1))



def build_coordinate_grid(height: int, width: int, device: torch.device | None = None) -> torch.Tensor:
    ys = torch.linspace(-1.0, 1.0, steps=height, device=device)
    xs = torch.linspace(-1.0, 1.0, steps=width, device=device)
    yy, xx = torch.meshgrid(ys, xs, indexing="ij")
    coords = torch.stack([xx, yy], dim=-1)
    return coords



def sample_random_points(
    sdf: torch.Tensor,
    corner: torch.Tensor,
    mask: torch.Tensor,
    coords_flat: torch.Tensor,
    points_per_sample: int,
) -> Dict[str, torch.Tensor]:
    # Input maps: [B, C, H, W]; coords_flat: [HW, 2]
    bsz, _, h, w = sdf.shape
    hw = h * w
    device = sdf.device
    idx = torch.randint(0, hw, (bsz, points_per_sample), device=device)

    sdf_flat = sdf.view(bsz, 3, hw).permute(0, 2, 1)
    corner_flat = corner.view(bsz, 3, hw).permute(0, 2, 1)
    mask_flat = mask.view(bsz, 3, hw).permute(0, 2, 1)

    idx_sdf = idx.unsqueeze(-1).expand(-1, -1, 3)
    gt_sdf = torch.gather(sdf_flat, 1, idx_sdf)
    gt_corner = torch.gather(corner_flat, 1, idx_sdf)
    gt_mask = torch.gather(mask_flat, 1, idx_sdf)

    coords = coords_flat[idx]
    return {"coords": coords, "sdf": gt_sdf, "corner": gt_corner, "mask": gt_mask}



def weighted_sdf_loss(pred_sdf: torch.Tensor, gt_sdf: torch.Tensor, delta: float, boost: float) -> torch.Tensor:
    weight = 1.0 + boost * torch.exp(-torch.abs(gt_sdf) / max(delta, 1e-6))
    return torch.mean(weight * F.smooth_l1_loss(pred_sdf, gt_sdf, reduction="none"))



def corner_loss(pred_corner_logits: torch.Tensor, gt_corner: torch.Tensor) -> torch.Tensor:
    pred_corner = torch.sigmoid(pred_corner_logits)
    return F.mse_loss(pred_corner, gt_corner)



def mask_loss(pred_mask_logits: torch.Tensor, gt_mask: torch.Tensor) -> torch.Tensor:
    return F.binary_cross_entropy_with_logits(pred_mask_logits, gt_mask)



def separation_loss(pred_mask_logits: torch.Tensor) -> torch.Tensor:
    p = torch.sigmoid(pred_mask_logits)
    pairwise = p[..., 0] * p[..., 1] + p[..., 0] * p[..., 2] + p[..., 1] * p[..., 2]
    return pairwise.mean()



def sdf_mask_consistency_loss(pred_sdf: torch.Tensor, pred_mask_logits: torch.Tensor, temperature: float) -> torch.Tensor:
    sdf_prob = torch.sigmoid(-pred_sdf / max(temperature, 1e-6))
    mask_prob = torch.sigmoid(pred_mask_logits)
    return F.mse_loss(mask_prob, sdf_prob)



def total_reconstruction_loss(
    outputs: Dict[str, torch.Tensor],
    gt_sdf: torch.Tensor,
    gt_corner: torch.Tensor,
    gt_mask: torch.Tensor,
    cfg: TrainConfig,
    latent: Optional[torch.Tensor] = None,
    mu: Optional[torch.Tensor] = None,
    logvar: Optional[torch.Tensor] = None,
) -> Dict[str, torch.Tensor]:
    loss_sdf = weighted_sdf_loss(outputs["sdf"], gt_sdf, cfg.boundary_delta, cfg.boundary_boost)
    loss_corner = corner_loss(outputs["corner_logits"], gt_corner)
    loss_mask = mask_loss(outputs["mask_logits"], gt_mask)
    loss_sep = separation_loss(outputs["mask_logits"])
    loss_cons = sdf_mask_consistency_loss(outputs["sdf"], outputs["mask_logits"], cfg.mask_temperature)

    total = (
        cfg.weight_sdf * loss_sdf
        + cfg.weight_corner * loss_corner
        + cfg.weight_mask * loss_mask
        + cfg.weight_sep * loss_sep
        + cfg.weight_consistency * loss_cons
    )

    loss_kl = torch.tensor(0.0, device=gt_sdf.device)
    if mu is not None and logvar is not None:
        loss_kl = kl_divergence(mu, logvar)
        total = total + cfg.beta_kl * loss_kl

    loss_latent_reg = torch.tensor(0.0, device=gt_sdf.device)
    if latent is not None:
        loss_latent_reg = torch.mean(latent ** 2)
        total = total + cfg.weight_latent_reg * loss_latent_reg

    return {
        "total": total,
        "sdf": loss_sdf,
        "corner": loss_corner,
        "mask": loss_mask,
        "sep": loss_sep,
        "consistency": loss_cons,
        "kl": loss_kl,
        "latent_reg": loss_latent_reg,
    }



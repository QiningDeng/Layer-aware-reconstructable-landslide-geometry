def imwrite_unicode(path: str | Path, image: np.ndarray) -> None:
    path = str(path)
    ext = os.path.splitext(path)[1]
    if not ext:
        raise ValueError(f"Output image path has no extension: {path}")
    ok, encoded = cv2.imencode(ext, image)
    if not ok:
        raise IOError(f"Failed to encode image: {path}")
    encoded.tofile(path)




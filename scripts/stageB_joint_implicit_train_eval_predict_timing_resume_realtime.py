def train_model(cfg: TrainConfig) -> None:
    requested_cfg = cfg
    device = default_device()
    resume_ckpt: Optional[Dict[str, Any]] = None
    resumed = bool(requested_cfg.resume_checkpoint)

    if resumed:
        resume_path = Path(requested_cfg.resume_checkpoint).expanduser().resolve()
        if not resume_path.is_file():
            raise FileNotFoundError(resume_path)
        resume_ckpt = load_checkpoint_compat(resume_path, device)
        saved_cfg = TrainConfig(**resume_ckpt["config"])

        # Preserve the original architecture, loss design, data split, and optimizer
        # hyperparameters. Only runtime continuation settings are overridden.
        saved_cfg.resume_checkpoint = str(resume_path)
        saved_cfg.resume_reset_optimizer = requested_cfg.resume_reset_optimizer
        saved_cfg.epochs = requested_cfg.epochs
        saved_cfg.patience = requested_cfg.patience
        saved_cfg.save_every = requested_cfg.save_every
        saved_cfg.plot_every = requested_cfg.plot_every
        saved_cfg.num_workers = requested_cfg.num_workers
        saved_cfg.cache_in_memory = requested_cfg.cache_in_memory
        saved_cfg.scheduler_type = requested_cfg.scheduler_type
        saved_cfg.scheduler_factor = requested_cfg.scheduler_factor
        saved_cfg.scheduler_patience = requested_cfg.scheduler_patience
        saved_cfg.min_learning_rate = requested_cfg.min_learning_rate
        saved_cfg.run_final_evaluation = requested_cfg.run_final_evaluation
        saved_cfg.evaluate_test_after_training = requested_cfg.evaluate_test_after_training
        if requested_cfg.feature_root:
            saved_cfg.feature_root = requested_cfg.feature_root
        if requested_cfg.output_root:
            saved_cfg.output_root = requested_cfg.output_root
        cfg = saved_cfg

        if resume_path.name == "best_model.pt":
            print(
                "[Warning] Resuming from best_model.pt. For an uninterrupted continuation, "
                "last_model.pt is normally preferable because it retains the latest epoch state."
            )
    else:
        cfg = requested_cfg

    set_seed(cfg.seed)

    if not cfg.feature_root:
        raise ValueError("feature_root is required.")
    if not cfg.output_root and not resumed:
        raise ValueError("output_root is required for a new training run.")

    if resumed:
        assert resume_ckpt is not None
        split_dict = resume_ckpt["split_dict"]
        output_dirs = resolve_output_structure_for_checkpoint(
            cfg.resume_checkpoint,
            cfg.output_root,
            resume_ckpt["model_type"],
            cfg.latent_dim,
        )
        cfg.model_type = str(resume_ckpt["model_type"])
    else:
        feature_files = discover_feature_files(cfg.feature_root)
        if not feature_files:
            raise RuntimeError("No Stage A feature files were found.")
        split_dict = split_feature_files(feature_files, cfg)
        output_dirs = create_output_structure(
            cfg.output_root, cfg.model_type, cfg.latent_dim
        )

    write_json(Path(output_dirs["logs"]) / "split_manifest.json", split_dict)
    write_json(Path(output_dirs["logs"]) / "train_config.json", asdict(cfg))

    if cfg.model_type == "autodecoder":
        latent_indices = {
            infer_sample_name(path): idx
            for idx, path in enumerate(split_dict["train"])
        }
    else:
        latent_indices = None

    train_set = StageAFeatureDataset(
        split_dict["train"],
        cfg.encoder_input_size,
        cfg.cache_in_memory,
        latent_indices,
    )
    val_set = StageAFeatureDataset(
        split_dict["val"],
        cfg.encoder_input_size,
        cfg.cache_in_memory,
    )
    test_set = StageAFeatureDataset(
        split_dict["test"],
        cfg.encoder_input_size,
        cfg.cache_in_memory,
    )

    train_loader = DataLoader(
        train_set,
        batch_size=cfg.batch_size,
        shuffle=True,
        num_workers=cfg.num_workers,
        collate_fn=collate_stageA,
    )
    val_loader = DataLoader(
        val_set,
        batch_size=1,
        shuffle=False,
        num_workers=cfg.num_workers,
        collate_fn=collate_stageA,
    )

    decoder = JointImplicitDecoder(
        latent_dim=cfg.latent_dim,
        hidden_dim=cfg.decoder_hidden_dim,
        num_layers=cfg.decoder_num_layers,
        fourier_levels=cfg.decoder_fourier_levels,
    ).to(device)

    encoder: Optional[ConvEncoder] = None
    embeddings: Optional[nn.Embedding] = None

    if cfg.model_type == "vae":
        encoder = ConvEncoder(in_channels=9, latent_dim=cfg.latent_dim).to(device)
        optimizer = torch.optim.AdamW(
            list(encoder.parameters()) + list(decoder.parameters()),
            lr=cfg.learning_rate,
            weight_decay=cfg.weight_decay,
        )
    elif cfg.model_type == "autodecoder":
        embeddings = nn.Embedding(
            len(split_dict["train"]), cfg.latent_dim
        ).to(device)
        nn.init.normal_(embeddings.weight, mean=0.0, std=0.01)
        optimizer = torch.optim.AdamW(
            [
                {"params": decoder.parameters(), "lr": cfg.learning_rate},
                {"params": embeddings.parameters(), "lr": cfg.latent_learning_rate},
            ],
            weight_decay=cfg.weight_decay,
        )
    else:
        raise ValueError("model_type must be 'vae' or 'autodecoder'.")

    scheduler = create_lr_scheduler(optimizer, cfg)

    history: List[Dict[str, Any]] = []
    best_score = float("inf")
    best_epoch = -1
    no_improve = 0
    start_epoch = 1

    if resumed:
        assert resume_ckpt is not None
        decoder.load_state_dict(resume_ckpt["decoder_state_dict"])
        if encoder is not None:
            encoder.load_state_dict(resume_ckpt["encoder_state_dict"])
        if embeddings is not None:
            saved_latents = resume_ckpt.get("latent_embeddings")
            if saved_latents is None:
                raise RuntimeError(
                    "The Auto-decoder resume checkpoint does not contain latent embeddings."
                )
            embeddings.weight.data.copy_(
                torch.as_tensor(
                    saved_latents, dtype=embeddings.weight.dtype, device=device
                )
            )

        if not cfg.resume_reset_optimizer:
            optimizer.load_state_dict(resume_ckpt["optimizer_state_dict"])
            move_optimizer_state_to_device(optimizer, device)
            scheduler_state = resume_ckpt.get("scheduler_state_dict")
            if scheduler is not None and scheduler_state is not None:
                scheduler.load_state_dict(scheduler_state)

        history = list(resume_ckpt.get("history", []))
        inferred_best_score, inferred_best_epoch, inferred_no_improve = (
            infer_best_state_from_history(history)
        )
        best_score = float(
            resume_ckpt.get("best_score", inferred_best_score)
        )
        best_epoch = int(
            resume_ckpt.get("best_epoch", inferred_best_epoch)
        )
        no_improve = int(
            resume_ckpt.get("no_improve", inferred_no_improve)
        )
        start_epoch = int(resume_ckpt.get("epoch", 0)) + 1

        if cfg.epochs < start_epoch:
            raise ValueError(
                f"Target epochs ({cfg.epochs}) must be >= resume start epoch "
                f"({start_epoch}). Use --epochs as the target total epoch."
            )

    print(f"Training has started. Model type: {cfg.model_type}", flush=True)
    print(f"Device: {device}", flush=True)
    print(
        f"Training samples: {len(train_set)} | Validation samples: {len(val_set)} "
        f"| Test samples: {len(test_set)}",
        flush=True,
    )
    if resumed:
        print(f"Resuming from: {cfg.resume_checkpoint}", flush=True)
        print(f"Continuation epoch range: {start_epoch} to {cfg.epochs}", flush=True)
    print(
        f"Scheduler: {cfg.scheduler_type} | Patience: {cfg.patience} | "
        f"Plot every: {cfg.plot_every} epoch(s)",
        flush=True,
    )

    if device.type == "cuda":
        torch.cuda.reset_peak_memory_stats(device)
    session_wall_start = time.perf_counter()
    if history:
        if "cumulative_compute_time_seconds" in history[-1]:
            previous_cumulative_compute = float(
                history[-1]["cumulative_compute_time_seconds"]
            )
        else:
            # Backward-compatible approximation for checkpoints produced by the
            # original script. Historical epoch_time_seconds values were not
            # CUDA-synchronized, so they are retained only as approximate timing.
            previous_cumulative_compute = float(
                sum(
                    float(
                        row.get(
                            "epoch_compute_time_seconds",
                            row.get("epoch_time_seconds", 0.0),
                        )
                    )
                    for row in history
                )
            )
    else:
        previous_cumulative_compute = 0.0

    for epoch in range(start_epoch, cfg.epochs + 1):
        train_timer = start_device_timer(device)
        if cfg.model_type == "vae":
            assert encoder is not None
            train_log = train_one_epoch_vae(
                encoder, decoder, train_loader, optimizer, cfg, device
            )
            latent_export = None
        else:
            assert embeddings is not None
            train_log = train_one_epoch_autodecoder(
                embeddings, decoder, train_loader, optimizer, cfg, device
            )
            latent_export = embeddings.weight.detach().cpu().numpy()
        train_time_seconds = stop_device_timer(train_timer, device)

        val_timer = start_device_timer(device)
        if cfg.model_type == "vae":
            assert encoder is not None
            val_log = validate_vae(encoder, decoder, val_loader, cfg, device)
        else:
            val_log = validate_autodecoder(decoder, val_loader, cfg, device)
        validation_time_seconds = stop_device_timer(val_timer, device)

        epoch_compute_time = train_time_seconds + validation_time_seconds
        previous_cumulative_compute += epoch_compute_time
        lrs_before_scheduler = current_learning_rates(optimizer)

        epoch_log: Dict[str, Any] = {
            "epoch": epoch,
            **train_log,
            **val_log,
            "train_time_seconds": float(train_time_seconds),
            "validation_time_seconds": float(validation_time_seconds),
            "epoch_compute_time_seconds": float(epoch_compute_time),
            "epoch_time_seconds": float(epoch_compute_time),
            "cumulative_compute_time_seconds": float(previous_cumulative_compute),
            "decoder_learning_rate": float(lrs_before_scheduler[0]),
            "latent_learning_rate_current": (
                float(lrs_before_scheduler[1])
                if len(lrs_before_scheduler) > 1
                else float("nan")
            ),
        }
        history.append(epoch_log)

        score = validation_selection_score(epoch_log)
        improved = (best_epoch < 0) or (score < best_score)
        if improved:
            best_score = score
            best_epoch = epoch
            no_improve = 0
        else:
            no_improve += 1

        if scheduler is not None:
            scheduler.step(score)
        epoch_log["validation_selection_score"] = float(score)
        epoch_log["no_improve_count"] = int(no_improve)
        epoch_log["learning_rate_after_scheduler"] = float(
            current_learning_rates(optimizer)[0]
        )

        scheduler_state = scheduler.state_dict() if scheduler is not None else None

        if improved:
            save_checkpoint(
                Path(output_dirs["checkpoints"]) / "best_model.pt",
                cfg.model_type,
                cfg,
                decoder,
                encoder,
                epoch,
                best_score,
                best_epoch,
                no_improve,
                history,
                split_dict,
                optimizer.state_dict(),
                scheduler_state=scheduler_state,
                latent_embeddings=latent_export,
            )

        if epoch % cfg.save_every == 0 or epoch == cfg.epochs:
            save_checkpoint(
                Path(output_dirs["checkpoints"]) / f"epoch_{epoch:04d}.pt",
                cfg.model_type,
                cfg,
                decoder,
                encoder,
                epoch,
                best_score,
                best_epoch,
                no_improve,
                history,
                split_dict,
                optimizer.state_dict(),
                scheduler_state=scheduler_state,
                latent_embeddings=latent_export,
            )

        print_detailed_epoch_progress(
            cfg=cfg,
            epoch=epoch,
            train_log=train_log,
            val_log=val_log,
            train_time_seconds=train_time_seconds,
            validation_time_seconds=validation_time_seconds,
            cumulative_compute_time_seconds=previous_cumulative_compute,
            score=score,
            improved=improved,
            best_epoch=best_epoch,
            best_score=best_score,
            no_improve=no_improve,
            optimizer=optimizer,
        )

        save_csv(
            Path(output_dirs["logs"]) / "training_history.csv",
            history,
            list(history[0].keys()),
        )
        if (
            cfg.plot_every > 0
            and (epoch % cfg.plot_every == 0 or epoch == cfg.epochs)
        ):
            plot_history(history, output_dirs["curves"])

        if no_improve >= cfg.patience:
            print(f"Early stopping was triggered at epoch {epoch}.", flush=True)
            break

    if not history:
        raise RuntimeError("No training history is available after training/resume.")

    session_wall_time = float(time.perf_counter() - session_wall_start)
    scheduler_state = scheduler.state_dict() if scheduler is not None else None
    save_checkpoint(
        Path(output_dirs["checkpoints"]) / "last_model.pt",
        cfg.model_type,
        cfg,
        decoder,
        encoder,
        history[-1]["epoch"],
        best_score,
        best_epoch,
        no_improve,
        history,
        split_dict,
        optimizer.state_dict(),
        scheduler_state=scheduler_state,
        latent_embeddings=(
            embeddings.weight.detach().cpu().numpy()
            if embeddings is not None
            else None
        ),
    )

    plot_history(history, output_dirs["curves"])
    save_csv(
        Path(output_dirs["logs"]) / "training_history.csv",
        history,
        list(history[0].keys()),
    )

    best_compute_time = float(
        sum(
            float(
                row.get(
                    "epoch_compute_time_seconds",
                    row.get("epoch_time_seconds", 0.0),
                )
            )
            for row in history
            if int(row["epoch"]) <= best_epoch
        )
    )
    historical_timing_complete = all(
        "train_time_seconds" in row
        and "validation_time_seconds" in row
        and "epoch_compute_time_seconds" in row
        for row in history
    )
    train_times = [float(row.get("train_time_seconds", np.nan)) for row in history]
    val_times = [float(row.get("validation_time_seconds", np.nan)) for row in history]
    peak_memory = (
        int(torch.cuda.max_memory_allocated(device))
        if device.type == "cuda"
        else 0
    )
    training_summary = {
        "model_type": cfg.model_type,
        "latent_dim": int(cfg.latent_dim),
        "resumed": resumed,
        "resume_checkpoint": cfg.resume_checkpoint,
        "start_epoch_this_session": int(start_epoch),
        "final_epoch": int(history[-1]["epoch"]),
        "target_epoch": int(cfg.epochs),
        "best_epoch": int(best_epoch),
        "best_validation_selection_score": float(best_score),
        "early_stopping_patience": int(cfg.patience),
        "no_improve_count_at_end": int(no_improve),
        "session_wall_time_seconds": float(session_wall_time),
        "historical_timing_complete": bool(historical_timing_complete),
        "historical_timing_note": (
            "All epochs use synchronized detailed timing."
            if historical_timing_complete
            else "Pre-resume epochs use approximate legacy epoch_time_seconds; "
                 "newly continued epochs use synchronized detailed timing."
        ),
        "cumulative_compute_time_seconds": float(
            history[-1].get("cumulative_compute_time_seconds", float("nan"))
        ),
        "compute_time_to_best_epoch_seconds": float(best_compute_time),
        "mean_train_time_per_epoch_seconds": float(np.nanmean(train_times)),
        "mean_validation_time_per_epoch_seconds": float(np.nanmean(val_times)),
        "device": device_metadata(device),
        "peak_gpu_memory_allocated_bytes": peak_memory,
        "decoder_parameter_count": int(count_parameters(decoder)),
        "encoder_parameter_count": int(
            count_parameters(encoder) if encoder is not None else 0
        ),
        "trainable_embedding_parameter_count": int(
            count_parameters(embeddings) if embeddings is not None else 0
        ),
    }
    write_json(
        Path(output_dirs["logs"]) / "training_time_summary.json",
        training_summary,
    )

    best_ckpt = str(Path(output_dirs["checkpoints"]) / "best_model.pt")
    if cfg.run_final_evaluation:
        evaluate_checkpoint_on_split(
            best_ckpt, cfg.feature_root, cfg.output_root, split_name="val"
        )
        if cfg.evaluate_test_after_training and split_dict["test"]:
            evaluate_checkpoint_on_split(
                best_ckpt, cfg.feature_root, cfg.output_root, split_name="test"
            )

    print("Training completed.", flush=True)
    print(f"Best checkpoint: {best_ckpt}", flush=True)
    print(f"Best epoch: {best_epoch}", flush=True)
    print(f"Session wall time: {session_wall_time:.3f} s", flush=True)
    print(f"Outputs were saved to: {output_dirs['root']}", flush=True)



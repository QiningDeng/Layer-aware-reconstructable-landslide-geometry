def optimize_latent_for_sample(
    decoder: JointImplicitDecoder,
    sample: Dict[str, torch.Tensor],
    cfg: TrainConfig,
    device: torch.device,
    steps: Optional[int] = None,
    learning_rate: Optional[float] = None,
) -> Tuple[torch.Tensor, Dict[str, float]]:
    decoder.eval()
    sdf = sample["sdf"].to(device)
    corner = sample["corner"].to(device)
    mask = sample["mask"].to(device)
    h, w = sdf.shape[-2:]
    coords_flat = build_coordinate_grid(h, w, device=device).view(-1, 2)

    actual_steps = int(cfg.auto_decoder_val_steps if steps is None else steps)
    actual_lr = float(cfg.auto_decoder_val_lr if learning_rate is None else learning_rate)

    latent = torch.zeros((1, cfg.latent_dim), device=device, requires_grad=True)
    optimizer = torch.optim.Adam([latent], lr=actual_lr)
    history_loss: List[float] = []
    start = start_device_timer(device)

    for _ in range(actual_steps):
        optimizer.zero_grad(set_to_none=True)
        sampled = sample_random_points(sdf, corner, mask, coords_flat, cfg.points_per_sample)
        outputs = decoder(latent, sampled["coords"])
        loss_dict = total_reconstruction_loss(
            outputs, sampled["sdf"], sampled["corner"], sampled["mask"], cfg, latent=latent
        )
        loss_dict["total"].backward()
        optimizer.step()
        history_loss.append(float(loss_dict["total"].detach().cpu()))

    elapsed = stop_device_timer(start, device)
    return latent.detach(), {
        "latent_optimization_steps": int(actual_steps),
        "latent_optimization_learning_rate": float(actual_lr),
        "latent_optimization_time_seconds": float(elapsed),
        "latent_optimization_final_loss": float(history_loss[-1]) if history_loss else float("nan"),
    }



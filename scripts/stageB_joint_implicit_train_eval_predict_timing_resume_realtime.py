def discover_feature_files(feature_root: str | Path) -> List[str]:
    feature_dir = Path(feature_root) / "features"
    if feature_dir.is_dir():
        files = sorted(str(p) for p in feature_dir.glob("*_features.npz"))
        if files:
            return files
    files = sorted(str(p) for p in Path(feature_root).glob("*_features.npz"))
    return files



def infer_sample_name(feature_file: str | Path) -> str:
    name = Path(feature_file).stem
    if name.endswith("_features"):
        name = name[:-len("_features")]
    return name



def split_feature_files(files: Sequence[str], cfg: TrainConfig) -> Dict[str, List[str]]:
    files = list(files)
    rng = random.Random(cfg.seed)
    rng.shuffle(files)

    n = len(files)
    n_train = max(1, int(round(n * cfg.train_ratio)))
    n_val = max(1, int(round(n * cfg.val_ratio))) if n >= 3 else max(0, n - n_train)
    if n_train + n_val >= n:
        n_val = max(0, n - n_train - 1)
    n_test = n - n_train - n_val
    if n_test <= 0 and n >= 3:
        n_test = 1
        n_train = max(1, n_train - 1)

    train_files = files[:n_train]
    val_files = files[n_train:n_train + n_val]
    test_files = files[n_train + n_val:]

    return {"train": train_files, "val": val_files, "test": test_files}


class StageAFeatureDataset(Dataset):
    """Dataset wrapper for Stage A *.npz files."""

    def __init__(
        self,
        feature_files: Sequence[str],
        encoder_input_size: int,
        cache_in_memory: bool = False,
        latent_indices: Optional[Dict[str, int]] = None,
    ) -> None:
        self.feature_files = list(feature_files)
        self.encoder_input_size = int(encoder_input_size)
        self.cache_in_memory = bool(cache_in_memory)
        self.latent_indices = latent_indices or {}
        self._cache: Dict[str, Dict[str, Any]] = {}

        if self.cache_in_memory:
            for path in self.feature_files:
                self._cache[path] = self._load_one(path)

    def __len__(self) -> int:
        return len(self.feature_files)

    def _load_one(self, path: str) -> Dict[str, Any]:
        with np.load(path, allow_pickle=True) as data:
            feature_tensor = data["feature_tensor"].astype(np.float32)
            sdf = data["sdf_normalized"].astype(np.float32)
            corner = data["corner_field"].astype(np.float32)
            mask = data["mask"].astype(np.float32)
            layer_names = [str(x) for x in data["layer_names"].tolist()]
            vertical_ranks = [str(x) for x in data["vertical_ranks"].tolist()]
            source_image = str(data["source_image"].tolist()) if "source_image" in data else infer_sample_name(path)

        feature_small = cv2.resize(
            np.transpose(feature_tensor, (1, 2, 0)),
            (self.encoder_input_size, self.encoder_input_size),
            interpolation=cv2.INTER_LINEAR,
        )
        feature_small = np.transpose(feature_small, (2, 0, 1)).astype(np.float32)

        sample_name = infer_sample_name(path)
        latent_index = int(self.latent_indices.get(sample_name, -1))

        return {
            "sample_name": sample_name,
            "feature_file": path,
            "feature_tensor": feature_tensor,
            "feature_small": feature_small,
            "sdf": sdf,
            "corner": corner,
            "mask": mask,
            "layer_names": layer_names,
            "vertical_ranks": vertical_ranks,
            "source_image": source_image,
            "latent_index": latent_index,
        }

    def __getitem__(self, index: int) -> Dict[str, Any]:
        path = self.feature_files[index]
        item = self._cache.get(path)
        if item is None:
            item = self._load_one(path)
        return item



def collate_stageA(batch: Sequence[Dict[str, Any]]) -> Dict[str, Any]:
    feature_tensor = torch.from_numpy(np.stack([b["feature_tensor"] for b in batch], axis=0))
    feature_small = torch.from_numpy(np.stack([b["feature_small"] for b in batch], axis=0))
